<script>

    $(document).ready(function() {
        $('.wizard-card-summary .remove-item').click(function () {
            $('.' + $(this).data('block')).find('.thumbnail-form-hide').click();
        });
    });

    function summaryUpdate() {
        $('.summary-data').toggle($('.wizard-card-data .thumbnail.selected').length > 0);
        $('.summary-data tr').hide();

        $('.summary-hosting').toggle($('.wizard-card-hosting .thumbnail.selected').length > 0);
        $('.summary-hosting tr').hide();

        $('.summary-software').toggle($('.wizard-card-software .thumbnail.selected').length > 0);
        $('.summary-software tr').hide();

        $('.summary-support').toggle($('.wizard-card-support .thumbnail.selected').length > 0);
        $('.summary-support tr').hide();

        $('.wizard-card .thumbnail.selected').each(function (i, item) {
            $('.' + $(this).data('summary')).show();
        });

        if ($('.wizard-card .thumbnail.selected').length > 0) {
            $('#send-order').removeAttr('disabled');
        } else {
            $('#send-order').attr('disabled', 'disabled');
        }

        $('.summary-none').toggle($('.wizard-card .thumbnail.selected').length == 0);
    }

</script>